export const composeValidators = (...validators) => value =>
  validators.reduce((error, validator) => error || validator(value), undefined)

export const required = message => value =>
  value ? undefined : message

export const matchesValue = (expectedValue, message) => value =>
  value === expectedValue ? undefined : message

export const minLength = (min, message) => value =>
  !value || value.length >= min ? undefined : message

export const maxLength = (max, message) => value =>
  !value || value.length <= max ? undefined : message

export const matchesRegex = (regex, message) => value =>
  regex.test(value) ? undefined : message

export const isEmail = message => matchesRegex(regexEmail, message)

export const requiredPhoneObj = message => value =>
  (value && value.phone_country && value.phone_number) ? undefined : message
